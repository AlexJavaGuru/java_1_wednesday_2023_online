package teacher.lesson_4_if_statement.lessoncode;

public class ConditionalStatement {

    public static void main(String[] args) {

        int x = 5;


        if (x > 10) {
            System.out.print("x > 10");
        } else if (x < 10) {
            System.out.print("x < 10");
        } else if (x == 10) {
            System.out.print("x == 10");
        } else if (x == 5) {
            System.out.println("IMPOSSIBLE!!!");
        }


    }
}
