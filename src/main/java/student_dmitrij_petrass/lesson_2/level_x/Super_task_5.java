package student_dmitrij_petrass.lesson_2.level_x;

public class Super_task_5 {
    /* начинаем работу с написанием кода всегда через PSVM*/

    public static void main(String[] args) {

        /* сейчас надо задать переменные. Они бывают разных типов */

        int firstType = 10;
        double secondType = 33.80;
        float pie = 3.14f;

        /* и так далее */
        /* после с этими переменными можно делать математические вычисления */

        double sum = firstType + secondType + pie;
        double multiplication = firstType * secondType * pie;
        double dividing = firstType / secondType / pie;
        double subtracting = firstType - secondType - pie;

        /* и выводить результат в консоль через sout */

        System.out.println("SUM = " + sum);
        System.out.println("MULTI = " + multiplication);
        System.out.println("DIVIDING = " + dividing);
        System.out.println("subtracting = " + subtracting);

        /* спасибо за внимание */
    }
}
