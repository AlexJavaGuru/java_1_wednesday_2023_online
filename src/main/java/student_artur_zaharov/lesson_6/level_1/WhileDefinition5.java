public class WhileDefinition5 {

    public static void main(String[] args) {
        int i = 1;
        while (i <= 100) {
            System.out.println(i);
            i++;
        }

// smotrja kakaja logica zadanija bila, pervij variant banalen s prediduwimi

        int j = 100;
        while (j >= 1){
            System.out.println(j);
            j--;
        }
    }
}
